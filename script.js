const toast = document.querySelector("[data-toast]");
let toastTimer;

function showToast(message) {
  toast.textContent = message;
  toast.classList.add("show");
  window.clearTimeout(toastTimer);
  toastTimer = window.setTimeout(() => toast.classList.remove("show"), 2200);
}

document.querySelectorAll("[data-copy-ip]").forEach((button) => {
  button.addEventListener("click", async () => {
    try {
      await navigator.clipboard.writeText(SITE_CONFIG.serverIp);
      showToast(t("copiedToast"));
      const label = button.querySelector("[data-copy-label]");
      if (label) {
        label.textContent = t("copiedShort");
        window.setTimeout(() => { label.textContent = t(label.dataset.i18n); }, 1800);
      }
    } catch {
      showToast(`${t("serverIpFallback")}: ${SITE_CONFIG.serverIp}`);
    }
  });
});

